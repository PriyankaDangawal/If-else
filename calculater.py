a=int(input("enter the number"))
b=int(input("enter the number"))
symbol=input("enter the symbol")
if symbol=="+":
    print(a+b)
elif symbol=="-":
    print(a-b)
elif symbol=="*":
    print(a*b)
elif symbol=="/":
    print(a/b)
elif symbol=="%":
    print(a%b)
elif symbol=="//":
    print(a//b)
else:
    print("thank you")